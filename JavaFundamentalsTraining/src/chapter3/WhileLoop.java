package chapter3;

public class WhileLoop {
    public static void main(String[] args) {
        //display java 5 times
        //declare and initialize the loop-control variable
        int count = 1;
        //test the loop-control variable
        while(count <= 5){
            System.out.print(count + "  ");
            //change the value of the loop-control variable
            count++;
        }
        System.out.println("===============================");


    }
}
